#include <unistd.h>

int	ft_int_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (write(1, "(null)", 6));
	while (str[i] != '\0')
		write(1, &str[i++], 1);
	return (i);
}
